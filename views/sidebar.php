<h2>What is a Minify?</h2>

<p>
  'Minify' is a plugin to minify JavaScript and/or CSS code and combine it
  to one file. This will increase performance of website.
</p>
<p>
  So you don't need to do HTTP request for dozens files, all can be combined 
  in one file on the fly.
</p>

